public class op_011_int_to_int_prefix {
  public static void main(String[] args) {
    int a = 10;
    System.out.print("a   = "); System.out.println( a);
    System.out.print("+a  = "); System.out.println( (+a));
    System.out.print("-a  = "); System.out.println( (-a));
    System.out.println("Done!");
  }
}
