/*
Cast to int: float to int, double to int.
*/

public class float_32_cast_to_int {
  public static void main(String[] args) {
    float f1 = 12.1F;
    double d1 = -23.1;
    System.out.print("        f1  = "); System.out.println(         f1);
    System.out.print("(int)   f1  = "); System.out.println( (int)   f1);
    System.out.print("        d1  = "); System.out.println(         d1);
    System.out.print("(int)   d1  = "); System.out.println( (int)   d1);
    System.out.println("Done!");
  }
}
