public class op_012_int_to_int_pre_inc_dec {
  public static void main(String[] args) {
    int a = 10;
    System.out.print("a   = "); System.out.println( a);
    System.out.print("++a = "); System.out.println( (++a));
    System.out.print("a   = "); System.out.println( a);
    System.out.print("++a = "); System.out.println( (--a));
    System.out.print("a   = "); System.out.println( a);
    System.out.println("Done!");
  }
}

