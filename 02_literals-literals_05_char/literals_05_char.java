//main character literals - the part supported by maude
public class literals_05_char {
	public static void main(String[] args) {
		System.out.println("Standart single char literals:");
    System.out.print(""); System.out.print('a'); System.out.print('A'); System.out.print('0'); System.out.print(' '); System.out.println('#');//aA0 #
    System.out.println("Single chars, same as in named escapes:");
    System.out.print(""); System.out.print('b'); System.out.print('t'); System.out.print('n'); System.out.print('f'); System.out.println('r');
		System.out.println("Done!");
	}
}
