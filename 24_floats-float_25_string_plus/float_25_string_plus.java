/*
String concatenation operator: positive, negative, positive sub-zero and negative sub-zero doubles + another String.
*/

public class float_25_string_plus {
  public static void main(String[] args) {
    System.out.println(" 1.1  = " +  1.1);
    System.out.println("-1.1  = " + -1.1);
    System.out.println(" 0.1  = " +  0.1);
    System.out.println("-0.1  = " + -0.1);
    System.out.println("Done!");
  }
}
