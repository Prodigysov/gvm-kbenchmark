/*
Simple double literal, like 1.23, 0.017: print it.
*/

public class float_11_literals_simple {
  public static void main(String[] args) {
    double f1 = 1.23;
    double f2 = 0.017;
    System.out.println("1.23  = " + f1);
    System.out.println("0.017 = " + f2);
    System.out.println("Done!");
  }
}
