/*
print(some double), println(some double).
*/

public class float_61_print {
  public static void main(String[] args) {
    double f1 = 1.23;
    System.out.println("print:");
    System.out.print(f1);
    System.out.println();
    System.out.println("println:");
    System.out.println(f1);
    System.out.println("Done!");
  }
}
