/*
Float and double literals with a letter at the end: 1.2f, 1.3F, 1.4d, 1.5D.
*/

public class float_12_literals_with_letter {
  public static void main(String[] args) {
    System.out.println("1.23f  = " + 1.23f);
    System.out.println("1.23F  = " + 1.23F);
    System.out.println("1.23d  = " + 1.23d);
    System.out.println("1.23D  = " + 1.23D);
    System.out.println("Done!");
  }
}
