/*
Conditional operator (one case: one op double, another one integer).
*/

public class float_24_cond {
  public static void main(String[] args) {
    System.out.print("true  ? 1.23 : 1  = "); System.out.println( (true  ? 1.23 : 1));
    System.out.print("false ? 1.23 : 1  = "); System.out.println( (false ? 1.23 : 1));
    System.out.println("Done!");
  }
}
