/*
Cast among floats: float to double, double to float, float to float.
*/

public class float_31_cast_float_double {
  public static void main(String[] args) {
    float f1 = 1.23F;
    double d1 = 0.017;
    System.out.println("(float) f1  = " + (float) f1);
    System.out.println("(double)f1  = " + (double)f1);
    System.out.println("(float) d1  = " + (float) d1);
    System.out.println("(double)d1  = " + (double)d1);
    System.out.println("Done!");
  }
}
